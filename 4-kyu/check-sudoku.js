function rowcheck(funcarr) {
    for (let i = 0; i < funcarr.length; i++) {
        let sorted = funcarr[i].sort();
        for (let j = 0; j < funcarr.length; j++) {
            if (sorted[j] == sorted[j + 1]) {
                return false
            }
        }
    }
    return true
}
function colcheck(func2arr){
    let answer=[];
    for (let i = 0; i < func2arr.length; i++) {
        let newarr=[];
        for (let j = 0; j < func2arr.length; j++) {
            newarr.push(func2arr[j][i])
        }
        answer.push(newarr)
    }
    return rowcheck(answer)
}
function validSolution(board){
    return colcheck(board)&&rowcheck(board);
}
